private void cargatabla()
{
//recuperar vector de lectores
  Vector destinos=myap.get_vectordestinos();
  for(int i=0; i<=destinos.size()-1;i++)
  {//obtener un lector en una posiciÃ³n
  Destinos undestino=myap.get_destino(i);
  //crear fila en blanco con 3 columnas
  Object[]fila=new Object[5];
  fila[0]=String.valueOf(undestino.get_iddestino());
  EmpresasMicro le=myap.get_empresa(undestino.get_idempresa());
  fila[1]=le.get_nomyap();
Localidad li=myap.get_localidad(undestino.get_idlocalidad());
  fila[2]=li.get_nomyap();
  

  
  fila[3]=undestino.get_fecha();
  
  fila[4]=undestino.get_hora();
  
  modelo.addRow(fila);
  }
  }
