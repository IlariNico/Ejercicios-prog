private void cargatabla()
{
//recuperar vector de lectores
  Vector milocalidad=miapp.get_vectorlocalidad();
  for(int i=0; i<=milocalidad.size()-1;i++)
  {//obtener un lector en una posiciÃ³n
  Localidad unalocalidad=miapp.get_localidad(i);
  //crear fila en blanco con 3 columnas
  Object[]fila=new Object[2];
  fila[0]=String.valueOf(unalocalidad.get_idlocalidad());
  fila[1]=unalocalidad.get_nomyap();
  
  modelo.addRow(fila);
  }
}
