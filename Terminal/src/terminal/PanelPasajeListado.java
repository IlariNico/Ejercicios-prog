private void cargatabla()
{
//recuperar vector de lectores
  Vector pasaje=myap.get_vectorpasaje();
  for(int i=0; i<=pasaje.size()-1;i++)
  {//obtener un lector en una posiciÃ³n
  Pasaje unpasaje=myap.get_pasaje(i);
  //crear fila en blanco con 3 columnas
  Object[]fila=new Object[7];
  fila[0]=String.valueOf(unpasaje.get_idpasaje());
  Destinos a=myap.get_destino(i);
  EmpresasMicro le=myap.get_empresa(a.get_idempresa());
  fila[1]=le.get_nomyap();
Localidad li=myap.get_localidad(unpasaje.get_iddestino());
  fila[2]=li.get_nomyap();
  Pasaje b=myap.get_pasaje(i);
  Pasajero pa=myap.get_pasajero(b.get_idpasajero());
  fila[3]=pa.get_nomyap();
  fila[4]=pa.get_dni();
  fila[5]=unpasaje.get_fecha();
  fila[6]=unpasaje.get_hora();
  
  modelo.addRow(fila);
  }
  }
