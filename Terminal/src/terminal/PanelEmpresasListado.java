   private void CreaDestinoEmpresa(int id){
       Vector destinos=miapp.get_vectordestinos();
       for(int i=0; i<=destinos.size()-1;i++)
       {//obtener un lector en una posiciÃ³n
        Destinos undestino=miapp.get_destino(i);
        if(undestino.get_idempresa()==id)
         {
          Object[]fila=new Object[4];

          EmpresasMicro le=miapp.get_empresa(undestino.get_idempresa());
          fila[0]=le.get_nomyap();
          Localidad li=miapp.get_localidad(undestino.get_idlocalidad());
          fila[1]=li.get_nomyap();
          fila[2]=undestino.get_fecha();
          fila[3]=undestino.get_hora();
          modelo.addRow(fila);
        }
       }
   }
